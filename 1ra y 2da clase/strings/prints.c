#include <stdio.h>

int main() 

{
    int entero = 12;
    char caracter = '$';
    float flotante = 3.14;

    printf("El numero es: %d\n", entero); //Asi se imprime un numero.
    printf("El caracter es: %c\n", caracter); //Asi se imprime un caracter.    //El \n se utiliza para el salto de linea
    printf("El flotante es: %f\n", flotante); // Asi se imprime un flotante.    

    return 0;
}

